package com.textbooks.entity;

import lombok.Data;

@Data
public class Textbooks {
    private String id;
    private String name;
    private double price;

    public Textbooks() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
}
